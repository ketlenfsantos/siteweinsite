// Função para obter a descrição correta com base nos dados do orçamento
function obterDescricaoProduto(categoria, opcao, identidadeVisual) {
  // Normalizar strings para 'yes'/'no' para identidadeVisual
  let idVis = 'no';
  if (identidadeVisual === 'yes' || identidadeVisual === 'Já possui' || identidadeVisual === true) {
    idVis = 'yes';
  }

  // Ajustes para categorias diferentes
  if (categoria === 'site') {
    const chave = `${opcao}_${idVis}`;
    if (produtosDescricao.site[chave]) {
      return produtosDescricao.site[chave];
    }
  }

  if (categoria === 'logo') {
    if (produtosDescricao.logo[opcao]) {
      return produtosDescricao.logo[opcao];
    }
  }

  if (categoria === 'social') {
    if (produtosDescricao.social[opcao]) {
      return produtosDescricao.social[opcao];
    }
  }

  if (categoria === 'stationery') {
    if (produtosDescricao.stationery[opcao]) {
      return produtosDescricao.stationery[opcao];
    }
  }

  // Retorno padrão caso não encontre
  return {
    nome: 'Produto padrão',
    descricao: 'Descrição padrão para o produto selecionado.'
  };
}
